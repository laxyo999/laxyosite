	<li>Here's what you'll do day-to-day:</li>
	<li>Ensure that merchants are fully equipped with the knowledge and infrastructure required to fulfill online orders</li>
	<li>Asset management, deployment and troubleshooting all technical issues</li>
	<li>Effective communication across departments to help maintain seamless operations on a day-to-day basis</li>
</ul>

